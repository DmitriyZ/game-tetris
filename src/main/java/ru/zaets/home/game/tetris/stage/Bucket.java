    public void fallDown(Figure figure) {
        this.figure = figure;
        line = 1;
    }

    public boolean down() {
        clean();
        int i = line - 1;

        for (int fi = figure.getHeight() - 1; fi >= 0; fi--) {
            if (i < 0) {
                continue;
            }
            for (int fj = 0; fj < figure.getWidth(); fj++) {
                bucket[i][column + fj] = figure.getTemplate()[fi][fj];
            }
            i--;
            if (i < 0) {
                break;
            }
        }


        if (line == bucket.length) {
            return false;
        } else {
            line++;
            return true;
        }
    }

    private void clean(){
        for (int i = 0; i < bucket.length; i++) {
            for (int j = 0; j < bucket[i].length; j++) {
                bucket[i][j] = def[i][j];
            }
        }
    }
